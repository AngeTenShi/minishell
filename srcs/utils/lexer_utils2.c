#include "../minishell.h"

int	ft_isnotspecial(char c)
{
	if (c == '>' || c == '<' || c == '|')
		return (0);
	return (1);
}

int	ft_isspace(char c)
{
	if (c == ' ' || c == '\n' || c == '\t' || c == '\r'
		|| c == '\v' || c == '\f')
		return (1);
	return (0);
}
